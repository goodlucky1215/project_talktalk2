package com.vo;

public class RoomVO {
	private int room_no = 0;
	private String room_name ="";
	private int room_typeIdentity = 0;
	public int getRoom_no() {
		return room_no;
	}
	public void setRoom_no(int room_no) {
		this.room_no = room_no;
	}
	public String getRoom_name() {
		return room_name;
	}
	public void setRoom_name(String room_name) {
		this.room_name = room_name;
	}
	public int getRoom_typeIdentity() {
		return room_typeIdentity;
	}
	public void setRoom_typeIdentity(int room_typeIdentity) {
		this.room_typeIdentity = room_typeIdentity;
	}
}
